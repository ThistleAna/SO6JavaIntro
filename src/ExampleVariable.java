public class ExampleVariable {
    public static void main(String[] args) {
        // introducing Harry Potter person
        String firstName = "Harry"; // string data type = contains text
        String lastName = "Potter";
        int age = 10; // int data type = contains whole number
        char gender = 'M'; // char data type = contains a single character
        Boolean isAttending = true; // boolean data type = contains true and false
        float weight = 35.5f; // double data type = contains decimal numbers

        System.out.println("Student first name = " + firstName);
        System.out.println("Student last name= " + lastName);
        System.out.println("Student age = " + age);
        System.out.println("Student gender = " + gender);
        System.out.println("Is student attending Hogwarts? " + isAttending);
    }
}
